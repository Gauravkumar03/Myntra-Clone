//                                              Display Data

function displaydata(mensData){

    document.querySelector("#container").innerHTML=""
    mensData.forEach(function(elem){
    
        var div=document.createElement("div")
   
        var image=document.createElement("img")
        image.src=elem.image_url
   
        var name=document.createElement("p")
        name.setAttribute("class","name")
        name.innerText=elem.name
        
        var price=document.createElement("h4")
        price.setAttribute("class","price1")
        price.innerText="Rs."+ elem.price
   
   
        var of=document.createElement("span")
        of.setAttribute("class","off")
        of.innerText=elem.off
   
       var s=document.createElement("span")
       s.setAttribute("class","s")
       s.innerText="Rs." + elem.pprice
       price.append(s,of)
   
       var col=document.createElement("p")
       col.setAttribute("class","color")

   
        var strike=document.createElement("h4")
        strike.setAttribute("class","strike")
        strike.innerText=elem.brand
   
        div.append(image,strike,name,price)
        document.querySelector("#container").append(div)
     })


   
}


